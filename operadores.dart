// Ponto de entrada do programa: tudo começa a ser executado aqui.
void main() {
  // Variáveis do tipo double guardam números com casas decimais (ex.: 10.0).
  double num1 = 10;
  double num2 = 15;

  // Operador + : soma dois valores.
  double resultado = num1 + num2;
  print('O resultado da soma é $resultado');

  // Operador - : subtrai o segundo valor do primeiro (num2 - num1).
  resultado = num2 - num1;
  print('O resultado da subtração é $resultado');

  // Operador / : divisão. O resultado em double pode ter casas decimais.
  resultado = 10 / 2;
  print('O resultado da divisão é $resultado');

  // Operador * : multiplicação.
  resultado = 30 * 10;
  print('O resultado da multiplicação é $resultado');
}
