/// Exemplo de tipos básicos: [String] e [int].
///
/// Demonstra concatenação com `+` para texto e interpolação
/// com `$idade` para incluir um número na mensagem.
void main() {
  /// Nome completo em uma única string.
  String nome = 'Joelmir Rogério Carvalho';
  print('Meu nome é: ' + nome);

  /// Idade como número inteiro ([int]).
  int idade = 35;
  print('A minha idade é $idade');
}
